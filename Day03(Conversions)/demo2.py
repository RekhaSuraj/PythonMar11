# Conversion from decimal to hexadecimal
# hexadecimal will have from 0-9 and A-F/a-f
# hexa values will have 0x as prefix
# hex(decimal number) to convert from decimal to hex number

h1 = 25
var1 = hex(h1)
print(var1)

# 0x19

print(0x19)
# 25

print(hex(98))
# 0x62

print(0x62)
# 98

var2 = 2001
print(hex(var2))
# 0x7d1

print(0x7d1)
# 2001