# Conversion from decimal to binary
# Binary has only 0 and 1, It is prefixed with 0b
# bin(decimalnumber) - in built method to convert to binary

# Syntax: bin()
var1 = 39
var2 = bin(var1)
print(var2)
# 0b100111

v1 = 102
v2 = bin(v1)
print('after converesion',v2)
# 0b1100110

print(0b1100110)
# 102

n1 = 56
print(bin(n1))
# 0b111000

print(0b111000)
# 56

print(hex(0b111000))
# 0x38

print(oct(0b111000))
# 0o70