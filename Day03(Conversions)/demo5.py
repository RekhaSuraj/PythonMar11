# Floor division (//)
# Divides the numbers and ignores the decimal part of the quotient

f1 = 20
f2 = 2
res = f1//f2
print(res)

# 10

f3 = 23
f4 = 2
res1 = f3//f4
print(res1)
# 11

# Modulus (%) - It will give us the remainder after division
m1 = 40
m2 = 10

res2 = m1 % m2
print('Modulus',res2)
# Modulus 0


m3 = 35
m4 = 2

res3 = m3 % m4
print(res3)
# 1


# Exponentiation (**) - e1 to the Power of e2
e1 = 5
e2 = 3

exp = e1**e2
print('Exponentation',exp)
# Exponentation 125

print(7**2)
# 49















