# Bitwise shiftleft (<<) - Adds or moves the bits to the left as specified by the number of position
# Syntax: var_name(decimal) << number of positions

a = 5
print(a << 1)
# 10

print(a << 2)
# 20


# Bitwise shiftright (>>) - Loosing the bits to the right as specified by the number of positions
# Syntax: var_name(decimal) >> number of positions

a = 5
print(a>>1)
# 2

print(a>>2)
# 1

