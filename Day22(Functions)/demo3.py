def arithmetic(n1,n2):
	print(f'Addition of {n1}+{n2}={n1+n2}')
	print(f'Subtraction of {n1}-{n2}={n1-n2}')
	print(f'Multiplication of {n1}*{n2}={n1*n2}')
	print(f'Division of {n1}/{n2}={n1/n2}')
	print(f'Modulus of {n1}%{n2}={n1%n2}')
	print(f'Floor Division of {n1}//{n2}={n1//n2}')


arithmetic(7,5)
# Addition of 7+5=12
# Subtraction of 7-5=2
# Multiplication of 7*5=35
# Division of 7/5=1.4
# Modulus of 7%5=2
# Floor Division of 7//5=1

print('*****************')

