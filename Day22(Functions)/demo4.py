def even_odd(num):
	if num%2 == 0:
		print(f'{num} is even number')
	else:
		print(f'{num} is odd number')


even_odd(6)
# 6 is even number

even_odd(9)
# 9 is odd number

n1 = int(input('Enter a number'))
even_odd(n1)


