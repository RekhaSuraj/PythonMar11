# String methods
str1 = 'Python Developer'
# upper() - Return a copy of the string converted to uppercase(Capitals)
print(str1.upper())
# PYTHON DEVELOPER

v = str1.upper()
print(v)
# PYTHON DEVELOPER

# lower() - Return a copy of the string converted to lowercase (small letter)
print(str1.lower())
# python developer

s = 'HELLO WOrld'
print(s.lower())
# hello world

# swapcase() - Convert uppercase characters to lowercase and lowercase characters to uppercase
print(str1.swapcase())
# pYTHON dEVELOPER

print(s.swapcase())
# hello woRLD


