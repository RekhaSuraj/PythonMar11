# string - a sequence of characters enclosed withon single quotes or double quotes

s1 = 'Welcome'
print(type(s1))
# <class 'str'>

s2 = "Python"
print(type(s2))
# <class 'str'>

# Converting string to integer
t1 = '20'
print(type(t1))
# <class 'str'>

a = int(t1)
print(a)
print(type(a))
# <class 'int'>

