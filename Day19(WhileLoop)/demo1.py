# While Loop :
# Iteration will return until the condition is True, then iteration will stop

# Syntax:
# while False:
# 	print('Inside while loop')

# print('When condition becomes False')

# In simple words, The while loop enables the Python program to repeat a set of operations while a particular 
# condition is true.
# When the condition becomes false, execution comes out of the loop immediately, 
# and the first statement after the while loop is executed.

# Print first 5 numbers using while loop
i = 0

while i <=5:
	print(i)
	i = i+1

