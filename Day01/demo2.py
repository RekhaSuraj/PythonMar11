# Variable declaration
# single variable assigned to single value

a = 20
print(a)
# 20

# multiple variables assigned to single value
x=y=z=10
print('x value is',x)
# x value is 10
print('y value is',y)
# y value is 10
print('z value is',z)
# z value is 10


# multiple variables assigned to multiple values in a single line
a=10; b=20; c=30; d = 40
print('a value is',a)
print('b value is',b)
print('c value is',c)
# a value is 10
# b value is 20
# c value is 30

# Multiple values to multiple variables without using semicolon in a single line
l,m,n = 40,50,60
print(l)
print(m)
print(n)
# 40
# 50
# 60
