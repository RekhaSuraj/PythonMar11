# Greater than or Equal to (>=) - Returns True is x is greater than or Equal to y

x = 20
y = 10
print(x>=y)
# True

z = 40
l = 40
print(z>=l)
# True

n = 20
m = 30
print(n>=m)
# False

# <= Lesser than or Equal to - Returns True if a is lesser than or Equal to y

a = 50
b = 30
print(a<=b)
# False

x = 60
y = 70
print(x<=y)
# True

print(20<=20)
# True






