# Logical Operators - There are 3 types : and, or , not
# Return boolean value - True or False

# and - Returns True if both values are True
# Returns False if either one value is False(even if one value is False)

# print(True and True)
# # True

# print(True and False)
# # False

# print(False and True)
# # False

# print(False and False)
# False

# or operator - Returns True if either one value is True
# Returns False if both values are False

print(True or False)
# True

print(False or True)
# True

print(True or True)
# True

print(False or False)
# False

